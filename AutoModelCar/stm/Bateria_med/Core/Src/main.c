void Set_LED_Color(uint8_t R, uint8_t G, uint8_t B) {
    uint32_t color = (G << 16) | (R << 8) | B;
    uint32_t indx = 0;
    for (int i = 23; i >= 0; i--) {
        if (color & (1 << i)) pwmData[indx] = 13;
        else pwmData[indx] = 6;
        indx++;
    }
    for (int i = 0; i < 50; i++) {
        pwmData[indx] = 0;
        indx++;
    }
    HAL_TIM_PWM_Start_DMA(&htim3, TIM_CHANNEL_2, (uint32_t *)pwmData, 74);
}

void Monitor_Bateria_NoBloqueante(void) {
    // Si ya pasó 1000 ms (1 segundo)
    if (HAL_GetTick() - timer_lectura_adc >= 1000) {
        timer_lectura_adc = HAL_GetTick();

        HAL_ADC_Start(&hadc1);
        if (HAL_ADC_PollForConversion(&hadc1, 10) == HAL_OK) {
            voltaje_bateria = HAL_ADC_GetValue(&hadc1) * 0.008864f;

            if (voltaje_bateria > 3.0 && voltaje_bateria < 6.6) {
                Set_LED_Color(255, 0, 0); // Rojo
                flag_bateria_baja = 1;    // Activar bandera
            } else if (voltaje_bateria >= 6.6) {
                Set_LED_Color(0, 255, 0); // Verde
                flag_bateria_baja = 0;
            } else {
                Set_LED_Color(0, 0, 255); // Azul
                flag_bateria_baja = 0;
            }
        }
        HAL_ADC_Stop(&hadc1);
    }
}

void Control_Buzzer_NoBloqueante(void) {
    if (flag_bateria_baja == 1) {
        // Controlar el ritmo: 200ms sonando, 200ms silencio
        if (HAL_GetTick() - timer_ritmo_alarma >= 200) {
            timer_ritmo_alarma = HAL_GetTick();
            estado_alarma = !estado_alarma;
        }

        // Si es momento de sonar
        if (estado_alarma == 1) {
            HAL_GPIO_TogglePin(GPIOA, GPIO_PIN_6);
            for(volatile int i=0; i<2000; i++); // Micro-retraso para simular la frecuencia
        } else {
            HAL_GPIO_WritePin(GPIOA, GPIO_PIN_6, GPIO_PIN_RESET);
        }
    } else {
        estado_alarma = 0;
        HAL_GPIO_WritePin(GPIOA, GPIO_PIN_6, GPIO_PIN_RESET);
    }
}
