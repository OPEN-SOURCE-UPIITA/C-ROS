/* USER CODE BEGIN 4 */
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart) {
    if (huart->Instance == USART1) {

        // Verificar que la trama tenga el inicio (AA 55) y el fin (FF) correctos
        if (rx_buffer[0] == 0xAA && rx_buffer[1] == 0x55 && rx_buffer[6] == 0xFF) {

            uint8_t dir_dc    = rx_buffer[3]; // 0=Stop, 1=Adelante, 2=Atrás
            uint8_t speed_dc  = rx_buffer[4]; // 0 a 100
            uint8_t dir_servo = rx_buffer[5]; // 0=Centro, 1=Izq, 2=Der

            uint32_t pwm_val = speed_dc * 10;

            // ================= CONTROL DEL SERVO (PC8) =================
            if (dir_servo == 1) {
                __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_3, 1300); // Izquierda
            } else if (dir_servo == 2) {
                __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_3, 1800); // Derecha
            } else {
                __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_3, 1500); // Centro
            }

            // ================= CONTROL MOTORES DC =================
            if (dir_dc == 1) {
                // ESTADO: ADELANTE
                HAL_GPIO_WritePin(GPIOD, GPIO_PIN_9, GPIO_PIN_SET);   // Prende LED PD9
                HAL_GPIO_WritePin(GPIOD, GPIO_PIN_10, GPIO_PIN_RESET);

                __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, 0);
                __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_2, pwm_val);
                __HAL_TIM_SET_COMPARE(&htim9, TIM_CHANNEL_1, 0);
                __HAL_TIM_SET_COMPARE(&htim9, TIM_CHANNEL_2, pwm_val);
            }
            else if (dir_dc == 2) {
                // ESTADO: REVERSA
                HAL_GPIO_WritePin(GPIOD, GPIO_PIN_9, GPIO_PIN_RESET);
                HAL_GPIO_WritePin(GPIOD, GPIO_PIN_10, GPIO_PIN_SET);  // Prende LED PD10

                __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, pwm_val);
                __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_2, 0);
                __HAL_TIM_SET_COMPARE(&htim9, TIM_CHANNEL_1, pwm_val);
                __HAL_TIM_SET_COMPARE(&htim9, TIM_CHANNEL_2, 0);
            }
            else {
                // ESTADO: DETENIDO (FRENO ACTIVO / CORTOCIRCUITO DINÁMICO)
                HAL_GPIO_WritePin(GPIOD, GPIO_PIN_9, GPIO_PIN_RESET);
                HAL_GPIO_WritePin(GPIOD, GPIO_PIN_10, GPIO_PIN_RESET);

                // Mandar el 100% (1000) a ambos pines bloquea los motores al instante
                __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, 1000);
                __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_2, 1000);
                __HAL_TIM_SET_COMPARE(&htim9, TIM_CHANNEL_1, 1000);
                __HAL_TIM_SET_COMPARE(&htim9, TIM_CHANNEL_2, 1000);
            }
        }
        // Limpiamos el buffer y nos preparamos para escuchar los siguientes 7 bytes
        rx_buffer[0] = 0;
        HAL_UART_Receive_IT(&huart1, rx_buffer, 7);
    }
}
